text = "  Hello, Python!  " 
print(text.strip()) # Remove spaces from beginning and end
print(text.lower()) # Convert all letters to lowercase
print(text.upper()) # Convert all letters to uppercase
print(text.replace("Python", "World")) # Replace "Python" with "World"
print(text.split(",")) # Split the string using comma
print("hello".capitalize()) # Make first letter uppercase
print("hello world".title()) # Make first letter of every word uppercase
print("python".startswith("py")) # Check if string starts with "py",true or false
