fruits = ["apple", "banana", "mango"]
fruits.append("orange")   # Add "orange" at the end of the list
fruits.remove("banana")   # Remove "banana" from the list
fruits.insert(1, "grape")   # Insert "grape" at index 1
print(fruits)   # Print the full list
print(len(fruits))   # Print total number of items in the list
print(fruits[0])   # Print the first item (index 0)
fruits.sort()   # Sort the list alphabetically
print(fruits)   # Print the sorted list
